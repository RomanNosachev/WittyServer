package com.wittyhome.module_base.task;

public class GroupDetails 
{
	private String id;
	
	private Integer total;
	
	public GroupDetails() {}
	
	public String getId() 
	{
		return id;
	}

	public void setId(String id) 
	{
		this.id = id;
	}

	public Integer getTotal() 
	{
		return total;
	}

	public void setTotal(Integer total) 
	{
		this.total = total;
	}
}
